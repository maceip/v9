#!/usr/bin/env node
/**
 * Build Node API surface artifacts for EdgeJS browser runtime.
 *
 * Generates:
 * 1) `napi-bridge/node-api-surface.generated.js` (module/export manifest)
 * 2) `napi-bridge/generated-node-api/*.js` (ESM wrappers per builtin module)
 * 3) `web/node-api-importmap.generated.json` (import-map entries for wrappers)
 */

import { builtinModules, createRequire } from 'node:module';
import { mkdirSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const BRIDGE_DIR = join(ROOT, 'napi-bridge');
const WRAPPER_DIR = join(BRIDGE_DIR, 'generated-node-api');
const GENERATED_SURFACE = join(BRIDGE_DIR, 'node-api-surface.generated.js');
const IMPORTMAP_JSON = join(ROOT, 'web', 'node-api-importmap.generated.json');
const REFERENCE_JSON = join(__dirname, 'node-exports-reference.json');

const require = createRequire(import.meta.url);

const MANUAL_MODULES = [
  'assert/strict',
  'diagnostics_channel',
  'dns/promises',
  'inspector/promises',
  'path/posix',
  'path/win32',
  'stream/consumers',
  'stream/promises',
  'stream/web',
  'timers/promises',
  'util/types',
];

const MANUAL_EXPORTS = {
  'assert/strict': ['AssertionError', 'CallTracker', 'deepStrictEqual', 'default', 'ok', 'strict', 'strictEqual'],
  'dns/promises': ['Resolver', 'default', 'getServers', 'lookup', 'lookupService', 'resolve', 'resolve4', 'resolve6'],
  'inspector/promises': ['Session', 'default'],
  'path/posix': ['basename', 'default', 'delimiter', 'dirname', 'extname', 'format', 'join', 'normalize', 'parse', 'relative', 'resolve', 'sep'],
  'path/win32': ['basename', 'default', 'delimiter', 'dirname', 'extname', 'format', 'join', 'normalize', 'parse', 'relative', 'resolve', 'sep'],
  'stream/consumers': ['arrayBuffer', 'blob', 'buffer', 'default', 'json', 'text'],
  'stream/promises': ['default', 'finished', 'pipeline'],
  'stream/web': ['ByteLengthQueuingStrategy', 'CompressionStream', 'CountQueuingStrategy', 'DecompressionStream', 'ReadableStream', 'TransformStream', 'WritableStream', 'default'],
  'timers/promises': ['default', 'scheduler', 'setImmediate', 'setInterval', 'setTimeout'],
  'util/types': ['default', 'isAnyArrayBuffer', 'isArrayBuffer', 'isArgumentsObject', 'isAsyncFunction', 'isDataView', 'isDate', 'isMap', 'isPromise', 'isRegExp', 'isSet', 'isTypedArray'],
};

function normalizeModuleName(name) {
  if (!name || name.startsWith('_')) return null;
  const clean = name.startsWith('node:') ? name.slice(5) : name;
  if (!clean || clean.startsWith('internal/')) return null;
  return clean;
}

function toSafeFileName(moduleName) {
  return moduleName.replace(/[^a-zA-Z0-9]+/g, '-').replace(/^-+|-+$/g, '') || 'module';
}

function isIdentifier(name) {
  return /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(name);
}

function collectExportNames(moduleName, referenceMap) {
  const names = new Set(referenceMap[moduleName] || []);
  const specifier = `node:${moduleName}`;

  try {
    const imported = require(specifier);
    if (imported && typeof imported === 'object') {
      for (const key of Object.keys(imported)) names.add(key);
    }
    if (typeof imported === 'function') names.add('default');
  } catch {
    // Best effort.
  }

  try {
    const imported = require(moduleName);
    if (imported && typeof imported === 'object') {
      for (const key of Object.keys(imported)) names.add(key);
    }
    if (typeof imported === 'function') names.add('default');
  } catch {
    // Best effort.
  }

  if (MANUAL_EXPORTS[moduleName]) {
    for (const key of MANUAL_EXPORTS[moduleName]) names.add(key);
  }

  const filtered = [...names]
    .filter((name) => name && !name.startsWith('_'))
    .sort();
  if (!filtered.includes('default')) filtered.push('default');
  return filtered;
}

function writeSurfaceFile(modules, exportMap) {
  const generatedAt = new Date().toISOString();
  const content = [
    '// Auto-generated by scripts/build-node-api-surface.mjs',
    `// Generated at ${generatedAt}`,
    '',
    `export const NODE_API_SURFACE_MODULES = ${JSON.stringify(modules, null, 2)};`,
    '',
    `export const NODE_API_SURFACE_EXPORTS = ${JSON.stringify(exportMap, null, 2)};`,
    '',
    'export default {',
    '  NODE_API_SURFACE_MODULES,',
    '  NODE_API_SURFACE_EXPORTS,',
    '};',
    '',
  ].join('\n');
  writeFileSync(GENERATED_SURFACE, content, 'utf8');
}

function writeWrappers(modules, exportMap) {
  rmSync(WRAPPER_DIR, { recursive: true, force: true });
  mkdirSync(WRAPPER_DIR, { recursive: true });

  for (const moduleName of modules) {
    const safeName = toSafeFileName(moduleName);
    const wrapperPath = join(WRAPPER_DIR, `${safeName}-exports.js`);
    const exports = (exportMap[moduleName] || []).filter((name) => name !== 'default' && isIdentifier(name));
    const lines = [
      `// Auto-generated ESM wrapper for node:${moduleName}`,
      "import { getNodeApiModule, subscribeNodeApiModule } from '../node-api-surface.js';",
      '',
      `let _defaultExport = getNodeApiModule('${moduleName}');`,
      `function _syncNodeApiModuleBindings() {`,
      `  const mod = getNodeApiModule('${moduleName}');`,
      '  _defaultExport = mod;',
      ...exports.map((name) => `  ${name} = mod.${name};`),
      '}',
      ...exports.map((name) => `export let ${name};`),
      'export { _defaultExport as default };',
      '_syncNodeApiModuleBindings();',
      `subscribeNodeApiModule('${moduleName}', _syncNodeApiModuleBindings);`,
      '',
    ];
    writeFileSync(wrapperPath, lines.join('\n'), 'utf8');
  }
}

function writeImportMap(modules) {
  const imports = {};
  for (const moduleName of modules) {
    const safeName = toSafeFileName(moduleName);
    const target = `../napi-bridge/generated-node-api/${safeName}-exports.js`;
    imports[moduleName] = target;
    imports[`node:${moduleName}`] = target;
  }
  writeFileSync(IMPORTMAP_JSON, `${JSON.stringify({ imports }, null, 2)}\n`, 'utf8');
}

function main() {
  const referenceMap = JSON.parse(readFileSync(REFERENCE_JSON, 'utf8'));
  const moduleSet = new Set(MANUAL_MODULES);
  for (const moduleName of Object.keys(referenceMap)) moduleSet.add(moduleName);
  for (const moduleName of builtinModules) {
    const normalized = normalizeModuleName(moduleName);
    if (normalized) moduleSet.add(normalized);
  }

  const modules = [...moduleSet].sort();
  const exportMap = {};
  for (const moduleName of modules) {
    exportMap[moduleName] = collectExportNames(moduleName, referenceMap);
  }

  writeSurfaceFile(modules, exportMap);
  writeWrappers(modules, exportMap);
  writeImportMap(modules);

  console.log(`Generated ${modules.length} module definitions.`);
  console.log(`- ${GENERATED_SURFACE.replace(ROOT + '/', '')}`);
  console.log(`- ${WRAPPER_DIR.replace(ROOT + '/', '')}/*.js`);
  console.log(`- ${IMPORTMAP_JSON.replace(ROOT + '/', '')}`);
}

main();
